#include <iostream>

using namespace std;

int main() {
    // Loop through numbers 0 to 99
    for (int i = 0; i <= 99; i++) {
        // Check if the number is odd
        if (i % 2 == 1) {
            // If the number is odd, print it to the console
            cout << i << endl;
        }
   
        // Open a file named "odd_numbers.txt" for writing
        ofstream outfile("odd_numbers.txt");
    
    
    }

    return 0;
}

